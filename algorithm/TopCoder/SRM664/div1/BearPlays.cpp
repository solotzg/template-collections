struct BearPlays {
  int pileSize(int A, int B, int K) {
    int N = A + B;
    if (A > B)
      swap(A, B);
    int res = A, p = 2;
    while (K) {
      if (K & 1)
        res = (ll)res * p % N;
      p = (ll)p * p % N;
      K >>= 1;
    }
    return min(res, N - res);
  }
};
